package com.apkide.ui.services.project.util;

public class ConfigureFileException extends Exception{
	public ConfigureFileException() {
	}

	public ConfigureFileException(String message) {
		super(message);
	}

	public ConfigureFileException(String message, Throwable cause) {
		super(message, cause);
	}

	public ConfigureFileException(Throwable cause) {
		super(cause);
	}
}
