package com.apkide.ui.views.editor.theme.error;

public class ColorSchemeException extends Exception {
	public ColorSchemeException() {
	}
	
	public ColorSchemeException(String message) {
		super(message);
	}
	
	public ColorSchemeException(String message, Throwable cause) {
		super(message, cause);
	}
	
	public ColorSchemeException(Throwable cause) {
		super(cause);
	}
	
	public ColorSchemeException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}
}
